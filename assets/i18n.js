// i18n Engine
// =========================================
(function() {
  const KEY = 'alfie-lang';
  const DEFAULT = 'zh';

  function getLang() {
    return localStorage.getItem(KEY) || DEFAULT;
  }

  function setLang(lang) {
    localStorage.setItem(KEY, lang);
    applyLang(lang);
  }

  function applyLang(lang) {
    const dict = I18N[lang] || I18N[DEFAULT];
    document.documentElement.lang = lang === 'en' ? 'en' : 'zh-CN';
    document.documentElement.setAttribute('data-lang', lang);

    // Text content (safe, escapes HTML)
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) el.textContent = dict[key];
    });
    // HTML content (allows <br>, <em>, etc — only use on trusted internal keys)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });
    // Placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key] !== undefined) el.placeholder = dict[key];
    });
    // Document title
    const titleEl = document.querySelector('title[data-i18n]');
    if (titleEl) {
      const key = titleEl.getAttribute('data-i18n');
      if (dict[key]) document.title = dict[key];
    }
    // Form alert text exposed via data-i18n-alert on form
    const forms = document.querySelectorAll('form[data-i18n-alert]');
    forms.forEach(f => {
      f.dataset.alertText = dict[f.getAttribute('data-i18n-alert')] || '';
    });

    // Toggle button label
    document.querySelectorAll('.lang-toggle').forEach(btn => {
      btn.textContent = lang === 'en' ? '中' : 'EN';
      btn.setAttribute('aria-label', lang === 'en' ? '切换到中文' : 'Switch to English');
    });
  }

  // expose
  window.AlfieI18n = { getLang, setLang, applyLang };

  // init on load
  document.addEventListener('DOMContentLoaded', () => {
    applyLang(getLang());
    document.querySelectorAll('.lang-toggle').forEach(btn => {
      btn.addEventListener('click', () => {
        setLang(getLang() === 'en' ? 'zh' : 'en');
      });
    });
  });
})();
